def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  dif = num1 - num2
  dif
end

def division(num1, num2)
  quot = num1 / num2
  quot
end

def multiplication(num1, num2)
  product = num1 * num2
  product
end

def modulo(num1, num2)
  remainder = num1 % num2
end

def square_root(num)
  result = Math.sqrt(num)
  result
end